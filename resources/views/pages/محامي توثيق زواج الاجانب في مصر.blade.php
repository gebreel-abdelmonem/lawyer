<ol>
<li>شهاده عدم ممانعه من سفاره الزوجه الاجنبية داخل مصر مصدق عليها من الخارجية المصريه ( الديانه &#8211; الحاله الاجتماعية) اذا كان احد الاطراف ليبي الرجوع الرئيس المكتب.</li>
<li>جواز سفر الزوجه الاجنبية + ختم الاقامه الرجوع لرئيس المكتب.</li>
<li>شهادتين فحص طبي لراغبى الزواج من اى مستشفى حكوميه او ای مرکز صحی .</li>
