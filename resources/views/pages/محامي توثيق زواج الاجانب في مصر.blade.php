<p><span style="text-decoration: underline; color: #800000; font-size: 14pt;"><strong>** ملاحظة : &#8211;</strong></span> على المصرية التي ترغب فى الزواج من اجنبي ( امريكي &#8211; بريطاني &#8211; كندى &#8211; النمسا &#8211; تونس &#8230;&#8230;) التوجهه إلى مصلحة الشهر العقارى والتوثيق الكائنة 57 ش رمسيس &#8211; القاهرة &#8211; وذلك لعمل استثناء من تقديم شهادتى السفارة حيث ان السفارات التى يحمل جنسيتها الاجانب المذكورين عاليه تقدم. ٌإقرارات من السفاره وليس موافقة سفارة + شهاده بالديانة ( تاكيد اسلام او شهاده بالديانه ) .</p>
