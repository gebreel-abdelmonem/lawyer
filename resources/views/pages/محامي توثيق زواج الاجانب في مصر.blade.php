<h3><span style="text-decoration: underline; color: #000000; font-family: 'arial black', sans-serif;"><strong>ثانيآ : الأوراق المطلوبه لزواج المصريه من الاجنبي : &#8211;</strong></span></h3>
<ol>
<li>شهاده عدم ممانعه من سفاره الزوج الاجنبي داخل مصر مصدق عليها من الخارجية المصرية مبين بها ( الديانة &#8211; المهنه &#8211;</li>
