<p>&#8211; بالنسبة للدول التي ليس لها تمثيل دبلوماسى فى مصر يلزم تقديم ما يفيد ( الديانة والحاله الاجتماعيه . وعدم الممانعة ) من الدولة التي تحمل جنسيتها المواطنة الاجنيبة ويتم التصديق عليها من السفاره المصرية بالخارج ثم التصديق عليه من الخارجيه المصريه .</p>
