<li>اذا كان أحد الأطراف ليبي أو الزوج اردنى الرجوع لرئيس المكتب .</li>
<li>جواز سفر الزوج الاجنبي + ختم الاقامة ختم الاقامة .</li>
<li>شهادتين فحص طبي لراغبى الزواج من اى مستشفى حكوميه او اي مرکز صحی .</li>
<li>2 شهود .</li>
<li>5 صور شخصيه للزوجين</li>
<li>بطاقة الزوجه المصرية + شهادة ميلاد مميكنة .</li>
<li>قيد طلاق في حال اذا كانت الزوجه مطلقه .</li>
